                          <p>O ponto mais alto da cidade, o morro pode ser visto de locais muito afastados do centro, permanecendo como um dos símbolos de São Roque para quem vem de fora. Seu nome significa em tupi-guarani “Morro Pelado”, por conta de sua vegetação rasteira e limpa, que embora com alguns buracos na subida até o topo, oferece uma vista espetacular de toda a cidade e da região ao redor. Desse modo, o Morro do Saboó também se torna um dos pontos mais visitados de São Roque.</p>
