                          </figure>
                          <p>Abrigando uma imagem do padroeiro da cidade, o Morro do Cruzeiro é um dos símbolos para os cristãos católicos de São Roque, representando o início das festividades municipais e uma proteção para a cidade como um todo. A novena que acontece no fim de julho atrai centenas de fiéis todos os anos, mantendo viva a chama da festividade religiosa.</p>
